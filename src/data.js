  export function getTags() {
    let tags = [];
    photos.forEach((element) => {
      element.album.forEach(elem => { 
        if (elem.collection) {
          elem.collection.forEach(el => { 
            el.tags.forEach(e => { 
              tags.push(e.replace(/-/g, ' ').replace(/ \d+$/, ""));
            });
          });  
        } else {
          elem.tags.forEach(el => { 
            tags.push(el.replace(/-/g, ' ').replace(/ \d+$/, ""));
          });   
        }
        
      })
    });
      
  
  
      return uniq(tags);
      
    }
  function uniq(a) {
    var seen = {};
    return a.filter(function(item) {
        return seen.hasOwnProperty(item) ? false : (seen[item] = true);
    });
}




